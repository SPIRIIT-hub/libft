#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t		i;
	size_t		j;

	if (dstsize < ft_strlen(dst))
		return (dstsize + ft_strlen(src));
	i = 0;
	j = 0;
	while (dst[i])
		i++;
	while (i < dstsize - 1 && src[j])
		dst[i++] = src[j++];
	dst[i] = '\0';
	while (src[j++])
		i++;
	return (i);
}
